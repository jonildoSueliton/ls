// Unary operators
var number = 10;
console.log(typeof number);

console.log(-number);

console.log(~15);
console.log(!15);

// Arithmetic operators
console.log(10/3);
console.log(10**3);
console.log(Math.pow(10, 3));

// Relational operators
console.log("" instanceof String);
console.log(new String() instanceof String);
